package utils;

public class ColoredText {
    public static final String RESET = "\u001B[0m";       // Reset dello stile
    public static final String RED = "\u001B[31m";       // Testo rosso
    public static final String GREEN = "\u001B[32m";     // Testo verde
    public static final String YELLOW = "\u001B[33m";    // Testo giallo
    public static final String BLUE = "\u001B[34m";      // Testo blu
    public static final String PURPLE = "\u001B[35m";    // Testo viola
    public static final String CYAN = "\u001B[36m";      // Testo ciano
    public static final String ORANGE = "\u001B[38;5;208m"; // Testo arancione
    public static final String LIGHT_GRAY = "\u001B[37m";    // Grigio chiaro (ANSI standard)
}
